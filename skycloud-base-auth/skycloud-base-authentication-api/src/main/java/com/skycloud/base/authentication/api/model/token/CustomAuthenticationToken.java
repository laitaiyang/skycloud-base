package com.skycloud.base.authentication.api.model.token;

import com.alibaba.fastjson.JSONObject;
import com.skycloud.base.authentication.api.model.dto.CustomLoginDTO;
import lombok.Getter;
import lombok.Setter;
import org.springframework.security.authentication.AbstractAuthenticationToken;
import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.oauth2.common.OAuth2AccessToken;

import java.util.Collection;

/**
 * @author
 */
public class CustomAuthenticationToken extends AbstractAuthenticationToken {

    private final Object principal;

    @Getter
    private JSONObject data;

    @Getter
    @Setter
    private OAuth2AccessToken oAuth2AccessToken;

    public CustomAuthenticationToken(CustomLoginDTO customLoginDto) {
        super(null);
        this.principal = customLoginDto;
        this.setAuthenticated(false);
    }

    public CustomAuthenticationToken(Object principal, Collection<? extends GrantedAuthority> authorities) {
        super(authorities);
        this.principal = principal;
        super.setAuthenticated(true);
    }

    public CustomAuthenticationToken(Object principal, Collection<? extends GrantedAuthority> authorities, JSONObject data) {
        super(authorities);
        this.principal = principal;
        this.data = data;
        super.setAuthenticated(true);
    }

    @Override
    public Object getCredentials() {
        return null;
    }

    @Override
    public Object getPrincipal() {
        return this.principal;
    }

    @Override
    public void setAuthenticated(boolean isAuthenticated) throws IllegalArgumentException {
        if (isAuthenticated) {
            throw new IllegalArgumentException("Cannot set this token to trusted - use constructor which takes a GrantedAuthority list instead");
        } else {
            super.setAuthenticated(false);
        }
    }

    @Override
    public void eraseCredentials() {
        super.eraseCredentials();
    }

}
